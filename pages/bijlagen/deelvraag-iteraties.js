          <p><span style={{fontWeight: 400}}>Er zijn maar enkele machines waar echt data van wordt opgeslagen. Dit programma heet: “tracksis”. In dit programma wordt enkel data bijgehouden gerelateerd aan een storing, dus geen sensor data maar wel welke machine het is, hoelang de storing duurt, wie de operator was bij de machine etc.&nbsp;</span></p>
